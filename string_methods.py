name='John Oliver 6588@##'
print(name.upper())     #prints the whole thing in upper case
print(name.lower())     #prints the whole thing in lower case
print(name.title())     #prints the first letter of the word in upper case and rest into lower
print(name.find('Oli'))   #Gives the position of the case sensitive word/letter/sentence else gives -1
print('J' in name)      #Gives boolean value for the given case sensitive word/letter/sentence